import java.util.List;

public class Student {

    String id, status, last, first;
    List<String> majors, minors;

    public Student() {
        this.id = id;
        this.status = status;
        this.last = last;
        this.first = first;
        this.majors = majors;
        this.minors = minors;
    }
}
